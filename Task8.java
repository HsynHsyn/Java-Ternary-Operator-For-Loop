/* Write a Java program to create a stair step pattern
#
	#
		#
			#
				#
					# */


package Day10_Ternary_Operator_For_Loop;

public class Task8 {

	public static void main(String[] args) {

		for (int i=0; i<6; i+=1) {
			
			
			for (int j=0; j<i; j+=1) {
				
				System.out.print("\t");


		}
			System.out.println("#");

	}

	}
}
